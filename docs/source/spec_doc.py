# ---------------------------------------------------------------------------
# generate
# ---------------------------------------------------------------------------


def generate() -> str:
    lines = [AUTOGEN_HEADER, BADGES, ""]

    # --- File hierarchy -------------------------------------------------------
    lines += [
        "File hierarchy",
        "--------------",
        "",
        "Every ``zea`` HDF5 file follows the layout shown below.",
        "",
        FILE_TREE,
        "",
    ]

    # --- Root attributes ------------------------------------------------------
    lines += [
        "Root attributes",
        "~~~~~~~~~~~~~~~",
        "",
        "Stored as HDF5 root-level attributes (not groups).",
        "",
        ROOT_ATTRS_TABLE,
        "",
    ]

    # --- Group reference tabs -------------------------------------------------
    lines += [
        "Group reference",
        "~~~~~~~~~~~~~~~",
        "",
        "Click a group tab to explore its fields.",
        "Fields marked :bdg-secondary:`optional` may be absent; all others are",
        ":bdg-danger:`required`.",
        "",
        ".. tab-set::",
        "",
    ]

    # ---- data tab ------------------------------------------------------------
    lines += [
        "   .. tab-item:: data",
        "      :sync: data",
        "",
        "      Data group containing raw channel data, derived pipeline products,",
        "      and optional grouped data products.",
        "",
        "      **Data fields**",
        "",
        rst_full_table(DataSpec, base_indent=2),
        "",
        "      **Grouped data products**",
        "",
        "      Each grouped data product is an HDF5 sub-group. Spatial map groups",
        "      (beamformed_data, envelope_data, image, segmentation, and custom maps)",
        "      also include a ``coordinates`` field (per-pixel Cartesian positions in",
        "      metres, shape ``(*spatial_dims, 3)`` where ``spatial_dims`` matches the spatial",
        "      (non-channel) dimensions of ``values``).",
        "      Custom spatial maps are also accepted — any extra key passed to",
        "      :class:`~zea.data.spec.DataSpec` is validated as a generic",
        "      :class:`~zea.data.spec.Map` sub-group.",
        "",
    ]

    map_classes = [
        ("aligned_data", AlignedData),
        ("beamformed_data", BeamformedData),
        ("envelope_data", EnvelopeData),
        ("image", Image),
        ("segmentation", Segmentation),
        ("sos_map", SosMap),
        ("strain_percentage_map", StrainPercentageMap),
        ("shear_wave_elastography_map", ShearWaveElastographyMap),
        ("tissue_doppler", TissueDopplerMap),
        ("color_doppler", ColorDopplerMap),
    ]

    for fname, cls in map_classes:
        desc = MAP_DESCRIPTIONS.get(fname, _first_sentence(cls.__doc__))
        p2 = _IND * 2
        p3 = _IND * 3
        lines += [
            f"{p2}.. dropdown:: ``{fname}``",
            "",
            f"{p3}{desc}",
            "",
            rst_compact_table(cls, base_indent=3),
            "",
        ]

    # ---- scan tab ------------------------------------------------------------
    lines += [
        "   .. tab-item:: scan",
        "      :sync: scan",
        "",
        "      Scan group with acquisition and transmit sequence parameters.",
        "",
        rst_full_table(ScanSpec, base_indent=2),
        "",
    ]
    # ---- probe tab -----------------------------------------------------------
    lines += [
        "   .. tab-item:: probe",
        "      :sync: probe",
        "",
        "      Probe group with probe geometry and frequency parameters.",
        "",
        rst_full_table(ProbeSpec, base_indent=2),
        "",
    ]
    # ---- metadata tab --------------------------------------------------------
    lines += [
        "   .. tab-item:: metadata",
        "      :sync: metadata",
        "",
        "      Optional metadata group for subject, acquisition context, annotations,",
        "      and extra time-series signals (ECG, voice narration, probe orientation).",
        "      Extra signal keys are accepted and validated as",
        "      :class:`~zea.data.spec.SignalND` sub-groups.",
        "",
        rst_full_table(MetadataSpec, base_indent=2),
        "",
        "      **Sub-groups**",
        "",
    ]
    meta_subs = [
        ("subject", Subject, True),
        ("annotations", Annotations, True),
        ("probe_pose", ProbePose, False),
        ("ecg / voice_narration", Signal1D, False),
    ]
    for label, cls, compact in meta_subs:
        lines += [
            rst_dropdown(
                cls,
                f"``{label}`` \u2014 {cls.__name__}",
                base_indent=2,
                compact=compact,
            ),
            "",
        ]

    # ---- metrics tab ---------------------------------------------------------
    lines += [
        "   .. tab-item:: metrics",
        "      :sync: metrics",
        "",
        "      Optional metrics group for acquisition-level quality and performance metrics.",
        "",
        rst_full_table(MetricsSpec, base_indent=2),
        "",
    ]

    return "\n".join(lines) + "\n"


if __name__ == "__main__":
    content = generate()
    OUT_PATH.write_text(content)
    print(f"Written: {OUT_PATH}")
