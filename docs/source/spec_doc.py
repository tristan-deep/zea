OUT_PATH = Path(__file__).parent / "_spec_ref.rst"

_IND = "   "  # 3-space RST indent unit

# ---------------------------------------------------------------------------
# helpers
# ---------------------------------------------------------------------------


def _dtype_str(dtype) -> str:
    """Return inline-code RST for a dtype — always in double backticks."""
    if isinstance(dtype, tuple):
        return " | ".join(_dtype_str(d) for d in dtype)
    # Python str and numpy str_ both render as ``str``
    if dtype is str or dtype is np.str_:
        return "``str``"
    # Map numpy bool_ to bool for readability
    if dtype is np.bool_:
        return "``bool``"
    if hasattr(dtype, "__name__"):
        return f"``{dtype.__name__}``"
    return f"``{dtype}``"


def _shape_str(shape) -> str:
    if isinstance(shape, tuple) and shape and isinstance(shape[0], tuple):
        return " or ".join(_shape_str(s) for s in shape)
    if shape == ():
        return "scalar"
    return "(" + ", ".join(str(s) for s in shape) + ")"


def _is_optional(cls, name: str) -> bool:
    try:
        for f in fields(cls):
            if f.name == name:
                return f.default is not MISSING or f.default_factory is not MISSING
    except TypeError:
        pass
    return False


def _first_sentence(docstring: str) -> str:
    for line in (docstring or "").splitlines():
        line = line.strip()
        if line:
            return line
    return ""


BADGES = """\
.. |badge-req| replace:: :bdg-danger:`required`
.. |badge-opt| replace:: :bdg-secondary:`optional`
"""

# ---------------------------------------------------------------------------
# Table renderers
# ---------------------------------------------------------------------------


def rst_full_table(cls, base_indent: int = 0) -> str:
    """Render a spec class as a list-table with all columns including description."""
    schema = cls.SCHEMA
    meta = getattr(cls, "FIELD_METADATA", {})
    p = _IND * base_indent

    rows = []
    for fname, fspec in schema.items():
        fm = meta.get(fname, {})
        unit = fm.get("unit", "\u2013")
        desc = fm.get("description", "")

        if "spec" in fspec:
            sub = fspec["spec"]
            type_s = f":class:`~zea.data.spec.{sub.__name__}`"
            shape_s = "group"
        else:
            type_s = _dtype_str(fspec.get("dtype", ""))
            shape_s = _shape_str(fspec.get("shape", ()))

        opt = _is_optional(cls, fname)
        badge = "|badge-opt|" if opt else "|badge-req|"
        rows.append((fname, type_s, shape_s, unit, desc, badge))

    lines = [
        f"{p}.. list-table::",
        f"{p}   :header-rows: 1",
        f"{p}   :widths: 22 16 18 7 27 10",
        f"{p}",
        f"{p}   * - Field",
        f"{p}     - Type",
        f"{p}     - Shape",
        f"{p}     - Unit",
        f"{p}     - Description",
        f"{p}     - ",
    ]
    for fname, type_s, shape_s, unit, desc, badge in rows:
        lines += [
            f"{p}   * - ``{fname}``",
            f"{p}     - {type_s}",
            f"{p}     - {shape_s}",
            f"{p}     - {unit}",
            f"{p}     - {desc}",
            f"{p}     - {badge}",
        ]
    return "\n".join(lines)


def rst_compact_table(cls, base_indent: int = 0) -> str:
    """Render a spec class as a compact table without a description column."""
    schema = cls.SCHEMA
    meta = getattr(cls, "FIELD_METADATA", {})
    p = _IND * base_indent

    rows = []
    for fname, fspec in schema.items():
        fm = meta.get(fname, {})
        unit = fm.get("unit", "\u2013")

        if "spec" in fspec:
            sub = fspec["spec"]
            type_s = f":class:`~zea.data.spec.{sub.__name__}`"
            shape_s = "group"
        else:
            type_s = _dtype_str(fspec.get("dtype", ""))
            shape_s = _shape_str(fspec.get("shape", ()))

        opt = _is_optional(cls, fname)
        badge = "|badge-opt|" if opt else "|badge-req|"
        rows.append((fname, type_s, shape_s, unit, badge))

    lines = [
        f"{p}.. list-table::",
        f"{p}   :header-rows: 1",
        f"{p}   :widths: 22 20 28 10 10",
        f"{p}",
        f"{p}   * - Field",
        f"{p}     - Type",
        f"{p}     - Shape",
        f"{p}     - Unit",
        f"{p}     - ",
    ]
    for fname, type_s, shape_s, unit, badge in rows:
        lines += [
            f"{p}   * - ``{fname}``",
            f"{p}     - {type_s}",
            f"{p}     - {shape_s}",
            f"{p}     - {unit}",
            f"{p}     - {badge}",
        ]
    return "\n".join(lines)


def rst_dropdown(cls, title, base_indent=0, open_=False, compact=False) -> str:
    """Render a spec class as a sphinx-design dropdown."""
    p = _IND * base_indent
    pi = _IND * (base_indent + 1)

    parts = [f"{p}.. dropdown:: {title}"]
    if open_:
        parts.append(f"{p}   :open:")
    parts.append("")

    doc_line = _first_sentence(cls.__doc__)
    if doc_line:
        parts.append(f"{pi}{doc_line}")
        parts.append("")

    table_fn = rst_compact_table if compact else rst_full_table
    parts.append(table_fn(cls, base_indent=base_indent + 1))
    parts.append("")

    return "\n".join(parts)


